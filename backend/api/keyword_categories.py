def get_all_categories():
    """获取所有一级分类"""
    return list(KEYWORD_CATEGORIES.keys())

def get_subcategories(category):
    """获取指定分类的二级分类"""
    return list(KEYWORD_CATEGORIES.get(category, {}).keys())

def get_keywords(category, subcategory=None):
    """获取指定分类和子分类的关键词"""
    if category not in KEYWORD_CATEGORIES:
        return []

    if subcategory:
        return KEYWORD_CATEGORIES[category].get(subcategory, [])
    else:
        # 返回该分类下所有关键词
        all_keywords = []
        for keywords in KEYWORD_CATEGORIES[category].values():
            all_keywords.extend(keywords)
        return all_keywords
